const INCOME_KEYWORDS = ['收入', '入', '赚', '工资', '奖金', '分红', '补贴', '退款', '回收', '稿费', '劳务', '介绍费', '佣金', '外快', '理财收益', '理赔', '报销', '转账', '收款']

function classify(text) {
  let maxScore = 0
  let bestCategory = '其他'
  let bestType = 'expense'

  // 检查收入关键词
  for (const kw of INCOME_KEYWORDS) {
    if (text.includes(kw)) {
      bestType = 'income'
      break
    }
  }

  // 匹配类目
  for (const [category, keywords] of Object.entries(CATEGORIES)) {
    for (const kw of keywords) {
      if (text.includes(kw)) {
        const score = kw.length / text.length
        if (score > maxScore) {
          maxScore = score
          bestCategory = category
        }
      }
    }
  }

  // 计算置信度 0.5-1.0
  let confidence = 0.5
  if (maxScore > 0) {
    confidence = Math.min(0.5 + maxScore * 5, 1.0)
  }
  if (bestType === 'income') {
    confidence = Math.min(confidence + 0.1, 1.0)
  }

  return { category: bestCategory, type: bestType, confidence }
}

exports.main = async (event, context) => {
  try {
    const { text } = event
    if (!text) return { code: -1, errMsg: '缺少 text 参数' }

    const result = classify(text)
    return { code: 0, data: result }
  } catch (err) {
    return { code: -1, errMsg: err.message }
  }
}